#include "libft.h"

char	*ft_itoa(int n)
{
	char	*str;

	str = (char *)malloc(2 * sizeof (char));
	if (str == NULL)
		return (NULL);
	if (n < 0)
	{
		str[0] = '-';
		str[1] = '\0';
		str = ft_strjoin(str, ft_itoa(-n));
	}
	else if (n >= 10)
		str = ft_strjoin(ft_itoa(n / 10), ft_itoa(n % 10));
	else if (n < 10 && n >= 0)
	{
		str[0] = n + '0';
		str[1] = '\0';
	}
	return (str);
}

int	main(void)
{
	int	num = 1235423;
	printf("%s", ft_itoa(num));
}	
